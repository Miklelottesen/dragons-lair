            <h3>Denmark wins the World Magic Cup</h3>
            <p>This weekend the 2014 World Magic Cup concluded with an epic victory by underdog Denmark over Greece. The Danish Magic team shocked the world of Magic with their win..</p>
            <p>In this battle, team Denmark was represented by Lars Birch, Simon Nielsen, Martin Muller and Thomas Enevoldsen, which names are now engraved on the world cup trophy. </p>
            <p></p>
            <h4>One in a million</h4>
            <p> Birch went on to say that the three lucky cards were a ”one in a million” and that no one had expected Denmark to win the tournament. They had hoped to finish in the top 32 out of the 72-nation competed, but they won the final by overcoming Team Greece in a nail-biting climax, thanks to some Danish luck in the final turns.
